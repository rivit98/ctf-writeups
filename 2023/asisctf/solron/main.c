uint64_t sum8(uint8_t v, int start) {
    uint64_t s = 0;
    uint8_t mask = 0b10000000;
    for (size_t i = 0; i < 8; i++)
    {
        s += (v & mask) ? seeds[start+i] : 0;
        mask >>= 1;
    }

    return s;
}


